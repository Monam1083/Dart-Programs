void main() {
  // if statement
  var is_login = true;
  if (is_login) {
    print("welcome Monam");
  }

  // if else statement
  var is_user = false;
  if (is_user) {
    print("welcome Monam"); // false
  } else {
    print("welcome Guest");
  }
  // if else if statement
  var a = 10;
  if (a == 20) {
    print("a is equal to 20");
  } else if (a == 10) {
    print("a is equal to 10");
  } else {
    print("invalid number ");
  }
}
